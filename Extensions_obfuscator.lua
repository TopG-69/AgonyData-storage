local Random = function()
	local RMath_Maxin = nil

	local R_num1 = math.random(1, 10) R_num2 = math.random(1, 10)

	if ((R_num1 - R_num2) == 5) then RMath_Maxin = 3
		elseif ((R_num1 - R_num2) < 5 and (R_num1 - R_num2) > 0) then RMath_Maxin = 1
		elseif ((R_num1 - R_num2) < 1) then RMath_Maxin = 2
		elseif ((R_num1 - R_num2) > 5) then RMath_Maxin = 2
	end

	return RMath_Maxin
end

local ShuffleArray = function(Array)
    for i = #Array, 2, -1 do
        local j = math.random(i)
        Array[i], Array[j] = Array[j], Array[i]
    end
end

